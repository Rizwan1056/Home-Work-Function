let add = (num1 : number, num2: number):number => {   // Arrow Function pass 2 parameter return type number
    let sum = num1 + num2;                            // in sum variable store 10 + 5 
    return sum;                                       // return 15
}
let result = add(10,5);                               // call function add pass 2 arguments 10 , 5   
console.log(result);                                  // O/P is 15
